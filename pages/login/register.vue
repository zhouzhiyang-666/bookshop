<template>
	<view class="register">
	
		<view class="content">
			<!-- 头部logo -->
			<view class="header">
				<image :src="logoImage"></image>
			</view>
			<!-- 主体 -->
			<view class="main">
				<wInput
					v-model="phoneData"
					type="text"
					maxlength="11"
					placeholder="手机号"
				></wInput>
				<wInput
					v-model="passData"
					type="password"
					maxlength="11"
					placeholder="登录密码"
					isShowPass
				></wInput>
				<wInput
					v-model="verCode"
					type="number"
					maxlength="4"
					placeholder="验证码"
					
					isShowCode
					ref="runCode"
					@setCode="getVerCode()"
				></wInput>
					
			</view>
				
			<wButton 
				text="注 册"
				:rotate="isRotate" 
				@click.native="startReg()"
			></wButton>
			
			<!-- 底部信息 -->
			<view class="footer">
				<text 
					@tap="isShowAgree" 
					class="cuIcon"
					:class="showAgree?'cuIcon-radiobox':'cuIcon-round'"
				>同意</text>
				<!-- 协议地址 -->
				<navigator url="" open-type="navigate">《协议》</navigator>
			</view>
		</view>
	</view>
</template>

<script>
	var _this;
	import wInput from '../../components/watch-login/watch-input.vue' //input
	import wButton from '../../components/watch-login/watch-button.vue' //button
	
	export default {
		data() {
			return {
				//logo图片 base64
				logoImage: '../../static/uni-center/logo.png',
				phoneData:'', // 用户/电话
				passData:'', //密码
				verCode:"", //验证码
				showAgree:true, //协议是否选择
				isRotate: false, //是否加载旋转
			}
		},
		components:{
			wInput,
			wButton,
		},
		mounted() {
			_this= this;
		},
		methods: {
			isShowAgree(){
				//是否选择协议
				_this.showAgree = !_this.showAgree;
			},
			getVerCode(){
				//获取验证码
				if (_this.phoneData.length != 11) {
					_this.$api.msg("手机号不正确");
				    return false;
				}
				console.log("获取验证码")
				this.$refs.runCode.$emit('runCode'); //触发倒计时
				_this.$api.msg("模拟倒计时触发");
				
				setTimeout(function(){
					_this.$refs.runCode.$emit('runCode',0); //假装模拟下需要 终止倒计时
					_this.$api.msg("模拟倒计时终止");
				},6000)
			},
		    startReg() {
				//注册
				if(this.isRotate){
					//判断是否加载中，避免重复点击请求
					return false;
				}
				if (this.showAgree == false) {
					_this.$api.msg("请先同意《协议》");
				    return false;
				}
				if (this.phoneData.length !=11) {
					_this.$api.msg("手机号不正确");
				    return false;
				}
		        if (this.passData.length < 6) {
					_this.$api.msg("密码无效");
		            return false;
		        }
				if (this.verCode.length != 4) {
					_this.$api.msg("验证码不正确");
				    return false;
				}
				try{
					uni.setStorage({
						key:'userInfo',
						data:{'phoneData':this.phoneData,'passData':this.passData},
					    success:function(){
					    	let st=uni.getStorage({key:'userInfo'});
					    	console.log(st.data);
					    	console.log("注册成功")
					    	_this.$api.msg("注册成功");
							
							setTimeout(function(){
								uni.navigateBack({
									delta:1
								});
							},2000);
					    }
					})
				}catch(e){
					//TODO handle the exception
					console.log(e.description);
				}finally{
					_this.isRotate=true;
					setTimeout(function(){
						_this.isRotate=false
					},3000);
				}
		    }
		}
	}
</script>

